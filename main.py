import time

def main():
    print("Вітаємо в грі-квесті 'Лабіринт Чарівних Кущів'!")
    time.sleep(1)
    print("Артур прибуває до кущів, які створюють лабіринт.")
    time.sleep(1)
    print("Щоб вийти з лабіринту, Артуру потрібно знайти магічний камінь, прихований серед кущів.")
    time.sleep(2)
    
    hints = [
        "Там, де розцвітає чотирилистий клевер.",
        "Під першою сосною, що зростає на південь.",
        "Там, де співають солов'ї."
    ]

    found_stone = False

    for i, hint in enumerate(hints):
        print(f"\nПідказка {i+1}: {hint}")
        answer = input("Введіть 'шукати', щоб шукати камінь: ").lower()
        
        if answer == 'шукати':
            time.sleep(1)
            if i == len(hints) - 1:
                found_stone = True
                print("Ви знайшли магічний камінь! Артур отримує невелику магічну силу.")
            else:
                print("Підказка виявилася не тут. Рухайтесь далі...")
                time.sleep(1)
        else:
            print("Ви вирішили не шукати, поверніться до пошуку.")
            time.sleep(1)
            break

    if found_stone:
        print("\nАртур вийшов з лабіринту, отримавши захист для майбутньої битви з драконом!")
    else:
        print("\nАртур не знайшов магічний камінь. Поверніться і спробуйте ще раз.")

if __name__ == "__main__":
    main()
